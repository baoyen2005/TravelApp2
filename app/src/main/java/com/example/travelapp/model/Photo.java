package com.example.travelapp.model;

public class Photo {
    private String url;
    private String name;

    public Photo() {
    }

    public Photo(String url, String name) {
        this.url = url;
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
